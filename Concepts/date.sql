| Expression                      | Output       | Description              |
| ------------------------------- | ------------ | ------------------------ |
| `DATE_FORMAT(date, "%Y-%m-%d")` | `2025-08-09` | Full date                |
| `DATE_FORMAT(date, "%b %Y")`    | `Aug 2025`   | Abbreviated month + year |
| `DATE_FORMAT(date, "%W")`       | `Saturday`   | Day of week              |
| `DATE_FORMAT(date, "%H:%i")`    | `15:30`      | Time (24h)               |
